export function computeInstallments(baseCost: number, duration: number): number[] {
  if (duration <= 1) return [baseCost];
  if (duration === 2) {
    const half = Math.floor(baseCost / 2);
    return [half, baseCost - half];
  }
  if (duration === 3) {
    const pay1 = Math.floor(baseCost * 0.4);
    const pay2 = Math.floor(baseCost * 0.3);
    const pay3 = baseCost - pay1 - pay2;
    return [pay1, pay2, pay3];
  }
  const part = Math.floor(baseCost / duration);
  const result = new Array(duration).fill(part);
  result[result.length - 1] = baseCost - part * (duration - 1);
  return result;
}

export function computeDirectInitiationCost(baseCost: number): number {
  return Math.ceil(baseCost * 1.15 * 10) / 10;
}
